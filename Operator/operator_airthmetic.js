let a = 2;

let b = 3;

console.log("a = ", a, "b = ",b);
console.log("a + b =", a + b);      // increment
console.log("a - b =", a - b);      // decrement
console.log("a / b =", a / b);      // division
console.log("a * b =", a * b);      // multiplication
console.log("a % b =", a % b);      // modulus
console.log("a ** b =", a ** b);        // exponentiation